    public void atualizarTabela() {
             while (true) {                 
                try {

                        if(jTextFieldPesquisar.getText().equals("")){
                    preencherTabelaPedidos("select *from compra order by data");
                    
                        }else{
                            mod.setPesquisa(jTextFieldPesquisar.getText());
                            control.Buscar(mod);
                            preencherTabelaPedidos("select *from compra where nome like'%" + mod.getPesquisa()+ "%'");
                      }
                        
                      if(jTextFieldPesquisarParcela.getText().equals("")){
                          preencherTabelaParcelas("select *from parcelas order by data");
                      }else{
                          modParcela.setBuscar(jTextFieldPesquisarParcela.getText());
                          daoParcela.Buscar(modParcela);
                          preencherTabelaParcelas("select *from parcelas where nome like '%" + modParcela.getBuscar()+ "%'");
                      }  
                        
        
            Thread.sleep(20000);
             } catch (InterruptedException ex) {
        Thread.currentThread().interrupt();
      }
    }
  }
 
