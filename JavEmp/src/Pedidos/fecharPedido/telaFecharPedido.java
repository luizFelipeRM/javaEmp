    public void preencherTabelaPedidos(String Sql){
          ArrayList dados = new ArrayList();
          String [] colunas = new String []{"ID","Nome","Valor","Quantidade","Data"};
          conex.conectar();
          conex.executaSql(Sql);        
          try{
              conex.rs.first();
              do{
                  dados.add(new Object[]{conex.rs.getInt("id"),conex.rs.getString("nome"),conex.rs.getString("valor"),conex.rs.getInt("quantidade"),conex.rs.getString("data")});
              }while(conex.rs.next());
          }catch(SQLException ex){
              
                      
          }
          modeloTabela modelo = new modeloTabela(dados, colunas);
          
          jTablePedidos.setModel(modelo);
          jTablePedidos.getColumnModel().getColumn(0).setPreferredWidth(200);
          jTablePedidos.getColumnModel().getColumn(0).setResizable(false);
          
          jTablePedidos.getColumnModel().getColumn(1).setPreferredWidth(365);
          jTablePedidos.getColumnModel().getColumn(1).setResizable(false);
          
          jTablePedidos.getColumnModel().getColumn(2).setPreferredWidth(315);
          jTablePedidos.getColumnModel().getColumn(2).setResizable(false);
          
          jTablePedidos.getColumnModel().getColumn(3).setPreferredWidth(340);
          jTablePedidos.getColumnModel().getColumn(3).setResizable(false);
          
          jTablePedidos.getColumnModel().getColumn(4).setPreferredWidth(100);
          jTablePedidos.getColumnModel().getColumn(4).setResizable(false);

          jTablePedidos.getTableHeader().setReorderingAllowed(false);
          jTablePedidos.setAutoResizeMode(jTablePedidos.AUTO_RESIZE_OFF);
          jTablePedidos.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
          conex.desconecta();
      }

