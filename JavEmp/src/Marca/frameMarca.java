    public void preencherTabela(String Sql){
          ArrayList dados = new ArrayList();
          String [] colunas = new String []{"ID","Nome"};
          conex.conectar();
          conex.executaSql(Sql);        
          try{
              conex.rs.first();
              do{
                  dados.add(new Object[]{conex.rs.getInt("id"),conex.rs.getString("nome")});
              }while(conex.rs.next());
          }catch(SQLException ex){
              //JOptionPane.showMessageDialog(null, "Erro ao preencher Arraylist"+ex); 
          }
          modeloTabela modelo = new modeloTabela(dados, colunas);
          
          jTableMarca.setModel(modelo);
          jTableMarca.getColumnModel().getColumn(0).setPreferredWidth(70);
          jTableMarca.getColumnModel().getColumn(0).setResizable(false);
          jTableMarca.getColumnModel().getColumn(1).setPreferredWidth(882);
          jTableMarca.getColumnModel().getColumn(1).setResizable(false);
          
          jTableMarca.getTableHeader().setReorderingAllowed(false);
          jTableMarca.setAutoResizeMode(jTableMarca.AUTO_RESIZE_OFF);
          jTableMarca.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
          conex.desconecta();
      }
    
    
    
    
    
    
