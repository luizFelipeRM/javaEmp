    private void jTableMarcaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableMarcaMouseClicked
        // TODO add your handling code here:
         String nome =""+jTableMarca.getValueAt(jTableMarca.getSelectedRow(), 1);
        conex.conectar();
        conex.executaSql("select *from marca where nome='"+nome+"'");
        try {
            flag = 2;
            jButtonNovo.setEnabled(false);
            jButtonEnviar.setEnabled(true);
            jButtonExcluir.setEnabled(true);
            jTextFieldNome.setEnabled(true);
            conex.rs.first();
            jTextFieldId.setText(String.valueOf(conex.rs.getInt("id")));
            jTextFieldNome.setText(conex.rs.getString("nome"));

        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Erro ao selecionar os dados "+ex);
        }

        conex.desconecta();
        
    }//GEN-LAST:event_jTableMarcaMouseClicked

